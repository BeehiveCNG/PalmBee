#!/usr/bin/env python3

import rclpy
from rclpy.node import Node
from geometry_msgs.msg import PoseStamped
from mavros_msgs.srv import CommandBool, SetMode
import math
import time

class GuidedMission(Node):
    def __init__(self):
        super().__init__('guided_mission')

        self.pub = self.create_publisher(
            PoseStamped,
            '/mavros/setpoint_position/local',
            10
        )

        self.arm_srv = self.create_client(CommandBool, '/mavros/cmd/arming')
        self.mode_srv = self.create_client(SetMode, '/mavros/set_mode')

        self.timer = self.create_timer(0.1, self.run)

        self.stage = 0
        self.start_time = self.get_clock().now()

    def set_guided(self):
        req = SetMode.Request()
        req.custom_mode = 'GUIDED'
        self.mode_srv.call_async(req)

    def arm(self):
        req = CommandBool.Request()
        req.value = True
        self.arm_srv.call_async(req)

    def publish_setpoint(self, x, y, z, yaw):
        msg = PoseStamped()
        msg.header.stamp = self.get_clock().now().to_msg()
        msg.header.frame_id = 'map'
        msg.pose.position.x = x
        msg.pose.position.y = y
        msg.pose.position.z = z

        # yaw → quaternion (Z axis)
        msg.pose.orientation.z = math.sin(yaw / 2.0)
        msg.pose.orientation.w = math.cos(yaw / 2.0)

        self.pub.publish(msg)

    def run(self):
        t = (self.get_clock().now() - self.start_time).nanoseconds * 1e-9

        # Stage 0: switch to GUIDED & arm
        if self.stage == 0:
            self.set_guided()
            self.arm()
            self.stage = 1
            self.start_time = self.get_clock().now()
            self.get_logger().info("GUIDED + ARM")

        # Stage 1: takeoff & hold at 2m
        elif self.stage == 1:
            self.publish_setpoint(0.0, 0.0, -2.0, 0.0)
            if t > 5.0:
                self.stage = 2
                self.start_time = self.get_clock().now()

        # Stage 2: move forward 3m
        elif self.stage == 2:
            self.publish_setpoint(3.0, 0.0, -2.0, 0.0)
            if t > 5.0:
                self.stage = 3
                self.start_time = self.get_clock().now()

        # Stage 3: rotate 180 deg
        elif self.stage == 3:
            self.publish_setpoint(3.0, 0.0, -2.0, math.pi)
            if t > 4.0:
                self.stage = 4
                self.start_time = self.get_clock().now()

        # Stage 4: return to start
        elif self.stage == 4:
            self.publish_setpoint(0.0, 0.0, -2.0, math.pi)
            if t > 5.0:
                self.stage = 5

        # Stage 5: hold
        elif self.stage == 5:
            self.publish_setpoint(0.0, 0.0, -2.0, math.pi)

def main():
    rclpy.init()
    node = GuidedMission()
    rclpy.spin(node)

if __name__ == '__main__':
    main()


